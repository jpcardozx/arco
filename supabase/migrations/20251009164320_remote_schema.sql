      current_count, max_limit
      USING HINT = 'Acesse https://arco.consultingarco.com/pricing para ver os planos';
  END IF;

  -- Increment counter
  UPDATE user_profiles
  SET monthly_analysis_count = monthly_analysis_count + 1,
      updated_at = NOW()
  WHERE id = NEW.user_id;

  RETURN NEW;
END;
