CREATE OR REPLACE FUNCTION public.audit_changes()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO 'public'
AS $function$
DECLARE
    user_email TEXT;
    user_role TEXT;
    changed_fields TEXT[];
    old_json JSONB;
    new_json JSONB;
BEGIN
    -- Buscar email e role do usuário
    SELECT email INTO user_email FROM auth.users WHERE id = auth.uid();
    SELECT (auth.jwt() ->> 'role')::text INTO user_role;
    
    -- DELETE
    IF TG_OP = 'DELETE' THEN
        old_json := row_to_json(OLD)::jsonb;
        
        INSERT INTO public.audit_log (
            user_id, 
            user_email, 
            user_role,
            action, 
            table_name, 
            record_id, 
            old_data
        )
        VALUES (
            auth.uid(), 
            user_email,
            user_role,
            'DELETE', 
            TG_TABLE_NAME, 
            OLD.id, 
            old_json
        );
        
        RETURN OLD;
    
    -- UPDATE
    ELSIF TG_OP = 'UPDATE' THEN
        old_json := row_to_json(OLD)::jsonb;
        new_json := row_to_json(NEW)::jsonb;
        
        -- Detectar quais campos mudaram
        SELECT ARRAY_AGG(key) INTO changed_fields
        FROM jsonb_each(old_json)
        WHERE old_json->key IS DISTINCT FROM new_json->key;
        
        -- Só registra se houver mudanças reais
        IF array_length(changed_fields, 1) > 0 THEN
            INSERT INTO public.audit_log (
                user_id, 
                user_email,
                user_role,
                action, 
                table_name, 
                record_id, 
                old_data, 
                new_data,
                changed_fields
            )
            VALUES (
                auth.uid(), 
                user_email,
                user_role,
                'UPDATE', 
                TG_TABLE_NAME, 
                NEW.id, 
                old_json, 
                new_json,
                changed_fields
            );
        END IF;
        
        RETURN NEW;
    
    -- INSERT
    ELSIF TG_OP = 'INSERT' THEN
        new_json := row_to_json(NEW)::jsonb;
        
        INSERT INTO public.audit_log (
            user_id, 
            user_email,
            user_role,
            action, 
            table_name, 
            record_id, 
            new_data
        )
        VALUES (
            auth.uid(), 
            user_email,
            user_role,
            'INSERT', 
            TG_TABLE_NAME, 
            NEW.id, 
            new_json
        );
        
        RETURN NEW;
    END IF;
    
    RETURN NULL;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.auto_assign_lead()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
AS $function$
DECLARE
  assigned_admin UUID;
BEGIN
  -- If lead already assigned, skip
  IF NEW.assigned_to IS NOT NULL THEN
    RETURN NEW;
  END IF;

  -- Find admin with least leads (round-robin assignment)
  SELECT up.id INTO assigned_admin
  FROM user_profiles up
  WHERE up.user_type = 'admin'
  ORDER BY (
    SELECT COUNT(*)
    FROM leads l
    WHERE l.assigned_to = up.id
  ) ASC
  LIMIT 1;

  -- If admin found, assign lead
  IF assigned_admin IS NOT NULL THEN
    NEW.assigned_to := assigned_admin;
    RAISE NOTICE 'Lead % auto-assigned to admin %', NEW.id, assigned_admin;
  END IF;

  RETURN NEW;
END;
$function$
;

